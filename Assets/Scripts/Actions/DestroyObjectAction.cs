public class DestroyObjectAction : MonoBehaviour
{

    // -------------------------------------------------------------------------
    #region Action Functions
    // -------------------------------------------------------------------------
    public void ActionDestroyObject(GameObject toDestroy)
    {
        // Tell Unity to destroy the object
        Destroy(toDestroy);
    }
